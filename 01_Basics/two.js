//while loop

let index = 0;
while (index <= 10) {
  console.log(`The value is ${index}`);
  index = index + 2;
}

let arr = 0;
const myArray = ["flash", "superman", "batman"];
while (arr <= myArray.length) {
  console.log(`the value is ${myArray}`);
  arr = arr + 1;
}

//do while loop

let score = 10;
do {
  console.log(`The score is ${score}`);
  score = score + 2;
} while (score <= 20);
